n = int(input("Introduce el primer número "))
m = int(input("Introduce el segundo número "))
c = n // m
r = n % m

print(f"La división de {n} entre {m} da un cociente {c} y un resto {r}, donde {n} y {m} son los números introducidos por el usuario, y {c} y {r} son el cociente y el resto de la división entera respectivamente.")